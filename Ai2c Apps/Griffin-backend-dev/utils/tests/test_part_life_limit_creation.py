def create_single_test_part_life_limit(
    model_name: str = "AH-64D",
    nomenclature: str = "Test Part Life",
    part_number: str = "7-12345678-9",
    nsn: str = "987654321",
    cage: str = "CAGE1",
    wuc: str = "WUC1",
    tracking_type: str = "Hours",
    maot: float = 5744,
    tbo: float = 1000,
    component_type: str = "RC",
):
    part_life = LCF_341_01_LifeLimit.objects.create(
        model_name=model_name,
        nomenclature=nomenclature,
        part_number=part_number,
        nsn=nsn,
        cage=cage,
        wuc=wuc,
        tracking_type=tracking_type,
        maot=maot,
        tbo=tbo,
        component_type=component_type,
    )
    return part_life


